				<br>
				O Colégio NDI – Núcleo de Desenvolvimento Intelectual é uma instituição de ensino com turmas do turmas do Infantil ao terceiro ano do Ensino Médio, e tem como foco o pleno desenvolvimento da criança e do jovem em suas habilidades e competências, prezando pelo ensino de excelência como também pela formação humanística dos seus alunos.<br>

				O início de sua trajetória como instituição remonta ao começo dos anos 2000 quando começou a atuar como cursinho pré-vestibular. Em julho de 2001, identificou-se no mercado uma oportunidade de expansão, assim, foi elaborado um planejamento a fim de implantar o novo empreendimento. Foi então que em 2002, o NDI tornou-se Colégio atuando com turmas do Ensino Fundamental II e Ensino Médio.<br>

				Ampliando a proposta pedagógica, em 2015 foi introduzido o segmento da Educação Infantil, finalizando deste modo, sua formação atual. De cursinho a colégio, o NDI gradativamente foi transformando-se na instituição que é hoje. E nestes anos de existência, vem oferecendo à comunidade campinense, dedicação e comprometimento nos serviços educacionais prestados.
